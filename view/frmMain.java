    private void btnDeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteUserActionPerformed
        int[] countUsers = tableUsers.getSelectedRows();
        if (countUsers.length == 0) {
            JOptionPane.showMessageDialog(null, "Bn cha chn ngi dng cn xa", "THNG BO", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean flag = false;
            for (int i = 0; i < countUsers.length; i++) {
                userController.delUser(tableUsers.getValueAt(countUsers[i], 1).toString());
                flag = true;
            }
            if (flag == true) {
                JOptionPane.showMessageDialog(null, "Xa thnh cng", "THNG BO", HEIGHT);
            } else {
                JOptionPane.showMessageDialog(null, "Xa tht bi", "THNG BO", ERROR);
            }
        }
        btnUsers.doClick();
    }//GEN-LAST:event_btnDeleteUserActionPerformed

